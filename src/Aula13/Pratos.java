package Aula13;

public class Pratos {
    private String nomePrato, obsPrato;
    private int qntdPrato;

    public String getNomePrato() {
        return nomePrato;
    }

    public void setNomePrato(String nomePrato) {
        this.nomePrato = nomePrato;
    }

    public String getObsPrato() {
        return obsPrato;
    }

    public void setObsPrato(String obsPrato) {
        this.obsPrato = obsPrato;
    }

    public int getQntdPrato() {
        return qntdPrato;
    }

    public void setQntdPrato(int qntdPrato) {
        this.qntdPrato = qntdPrato;
    }
}
